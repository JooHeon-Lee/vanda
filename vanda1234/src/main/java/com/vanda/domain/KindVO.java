package com.vanda.domain;

public class KindVO {
	private int kind_num;
	private String kind_name;
	private double std_low;
	private double std_high;
	
	public int getKind_num() {
		return kind_num;
	}
	public void setKind_num(int kind_num) {
		this.kind_num = kind_num;
	}
	public String getKind_name() {
		return kind_name;
	}
	public void setKind_name(String kind_name) {
		this.kind_name = kind_name;
	}
	public double getStd_low() {
		return std_low;
	}
	public void setStd_low(double std_low) {
		this.std_low = std_low;
	}
	public double getStd_high() {
		return std_high;
	}
	public void setStd_high(double std_high) {
		this.std_high = std_high;
	}
	
	
	
	

}
